<body>
    <main class="main1">
        <h2>Políticas de Gestión de Investigación</h2>
        <p>
            En la Universidad de Huánuco, consideramos que la investigación es un pilar fundamental para el desarrollo académico, científico y social. Por ello, hemos desarrollado una serie de políticas que guían nuestros procesos de investigación para garantizar la calidad, integridad y relevancia de nuestros proyectos.
        </p>

        <h3>1. Compromiso con la Ética</h3>
        <ul>
            <li>Fomentar la honestidad y la integridad en todas las etapas del proceso de investigación.</li>
            <li>Evitar el plagio, la manipulación de datos y cualquier práctica que comprometa la veracidad de los resultados.</li>
            <li>Garantizar la protección de los derechos humanos y animales en los proyectos que involucren experimentación.</li>
        </ul>

        <h3>2. Calidad en los Proyectos de Investigación</h3>
        <ul>
            <li>Promover el uso de metodologías científicas sólidas y validadas.</li>
            <li>Evaluar los proyectos de investigación a través de comités especializados antes de su ejecución.</li>
            <li>Garantizar que los resultados de las investigaciones sean publicados en revistas reconocidas y accesibles.</li>
        </ul>

        <h3>3. Inclusión y Colaboración</h3>
        <ul>
            <li>Fomentar la participación de estudiantes y docentes en proyectos interdisciplinarios.</li>
            <li>Promover la colaboración con otras universidades y centros de investigación a nivel nacional e internacional.</li>
            <li>Garantizar la igualdad de oportunidades para todos los miembros de la comunidad universitaria en el acceso a fondos y recursos de investigación.</li>
        </ul>

        <h3>4. Uso Responsable de Recursos</h3>
        <ul>
            <li>Asegurar la gestión eficiente y transparente de los fondos destinados a la investigación.</li>
            <li>Optimizar el uso de laboratorios, equipos e instalaciones universitarias.</li>
            <li>Garantizar la sostenibilidad ambiental en todos los proyectos de investigación.</li>
        </ul>

        <h3>5. Protección de la Propiedad Intelectual</h3>
        <ul>
            <li>Reconocer los derechos de autor y de propiedad intelectual de los investigadores.</li>
            <li>Promover la protección de innovaciones y descubrimientos a través de patentes y otros mecanismos legales.</li>
        </ul>

        <p>
            Estas políticas están diseñadas para guiar nuestras acciones y reflejar nuestro compromiso con la excelencia académica y el desarrollo sostenible a través de la investigación.
        </p>
